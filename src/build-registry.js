#!/usr/bin/env node
"use strict";

const fs = require("fs");
const path = require("path");

/**
 * Scan .claude/skills/ and generate ontology files.
 * @param {string} targetDir - project root
 */
function buildRegistry(targetDir) {
  const skillsDir = path.join(targetDir, ".claude", "skills");
  const ontologyDir = path.join(targetDir, ".claude", "ontology");

  fs.mkdirSync(ontologyDir, { recursive: true });

  if (!fs.existsSync(skillsDir)) {
    console.log("No .claude/skills/ directory found");
    return 0;
  }

  const today = new Date().toISOString().slice(0, 10);
  const lines = [
    "# Skills Registry — machine-readable index",
    "# Auto-generated by build-registry.js",
    `# Last built: ${today}`,
    "",
    "skills:",
  ];

  let count = 0;

  for (const name of fs.readdirSync(skillsDir).sort()) {
    const dir = path.join(skillsDir, name);
    if (!fs.statSync(dir).isDirectory()) continue;

    // Find SKILL.md or skill.md
    let skillFile;
    for (const candidate of ["SKILL.md", "skill.md"]) {
      const p = path.join(dir, candidate);
      if (fs.existsSync(p)) {
        skillFile = p;
        break;
      }
    }
    if (!skillFile) continue;

    const content = fs.readFileSync(skillFile, "utf-8");
    const meta = parseFrontmatter(content);
    const fileLines = content.split("\n").length;
    const tokenEstimate = Math.round(fileLines * 3.8);

    const version = meta.version || "1.0.0";
    const description = meta.description || "";
    const autoTrigger =
      /auto[_-]?trigger|auto[_-]?load/i.test(description);
    const invocation = autoTrigger ? "null" : `"/${name}"`;
    const triggers = extractTriggers(description);
    const domain = inferDomain(name);
    const phase = inferPhase(name);

    lines.push(
      "",
      `  ${name}:`,
      `    version: "${version}"`,
      `    invocation: ${invocation}`,
      `    auto_trigger: ${autoTrigger}`,
      `    domain: [${domain}]`,
      `    phase: [${phase}]`,
      `    triggers: [${triggers}]`,
      `    file: ".claude/skills/${name}/${path.basename(skillFile)}"`,
      `    token_estimate: ${tokenEstimate}`
    );

    count++;
  }

  fs.writeFileSync(path.join(ontologyDir, "registry.yaml"), lines.join("\n") + "\n");
  console.log(`Registry: ${count} skills indexed`);

  // Create graph.yaml skeleton if not exists
  const graphPath = path.join(ontologyDir, "graph.yaml");
  if (!fs.existsSync(graphPath)) {
    fs.writeFileSync(
      graphPath,
      [
        "# Skills Relationship Graph — edges between skills",
        "# 6 types: prerequisite, complementary, alternative, orchestrates, evolves, enhances",
        "",
        "edges: []",
        "# Edge schema:",
        "#   - from: skill-a",
        "#     to: skill-b",
        "#     type: prerequisite",
        "#     strength: 80",
        '#     note: "why"',
        "",
      ].join("\n")
    );
    console.log("Graph: created skeleton (add edges manually)");
  } else {
    console.log("Graph: already exists (kept)");
  }

  // Create chains.yaml skeleton if not exists
  const chainsPath = path.join(ontologyDir, "chains.yaml");
  if (!fs.existsSync(chainsPath)) {
    fs.writeFileSync(
      chainsPath,
      [
        "# Pre-defined Skill Chains — common task sequences",
        "",
        "chains: {}",
        "# Chain schema:",
        "#   chain-name:",
        '#     description: "what this chain does"',
        '#     when: "when to use it"',
        "#     skills: [skill-a, skill-b]",
        "#     optional: [skill-b]",
        "#     estimated_tokens: 2000",
        "",
      ].join("\n")
    );
    console.log("Chains: created skeleton (add chains manually)");
  } else {
    console.log("Chains: already exists (kept)");
  }

  return count;
}

/** Parse YAML frontmatter from --- delimited block */
function parseFrontmatter(content) {
  const match = content.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return {};
  const block = match[1];
  const meta = {};
  for (const line of block.split("\n")) {
    const m = line.match(/^(\w+):\s*(.+)/);
    if (m) {
      let val = m[2].trim();
      // Strip quotes
      if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
        val = val.slice(1, -1);
      }
      // Strip YAML pipe indicator
      if (val === "|") {
        // Multi-line: collect indented lines
        const idx = block.indexOf(line);
        const rest = block.slice(idx + line.length + 1);
        const multiLines = [];
        for (const l of rest.split("\n")) {
          if (/^\s+/.test(l)) multiLines.push(l.trim());
          else break;
        }
        val = multiLines.join(" ");
      }
      meta[m[1]] = val;
    }
  }
  return meta;
}

/** Extract trigger keywords from description text */
function extractTriggers(description) {
  const triggerMatch = description.match(/[Tt]riggers?[:\s]+(.+)/);
  if (!triggerMatch) return "";
  return triggerMatch[1]
    .replace(/[,.]/g, ", ")
    .replace(/\s+/g, " ")
    .trim()
    .slice(0, 200);
}

/** Infer domain from skill name */
function inferDomain(name) {
  if (/^vbounce|^framework-|^prd$|^brainstorming$|^test-driven|^review-prd$/.test(name)) return "sdlc";
  if (/^frontend-|^ui-|^vercel-/.test(name)) return "frontend";
  if (/^azdo-|^cicd-/.test(name)) return "devops";
  if (/^aiquinta-/.test(name)) return "aiquinta";
  if (/^python-|^async-python/.test(name)) return "backend";
  if (/^claude-agent/.test(name)) return "sdk";
  if (/^mermaid/.test(name)) return "documentation";
  if (/^skill-|^find-/.test(name)) return "meta";
  return "general";
}

/** Infer SDLC phase from skill name */
function inferPhase(name) {
  if (/brainstorming|^prd$|requirements|review-prd/.test(name)) return "requirements";
  if (/planner|design/.test(name)) return "design";
  if (/implement|frontend-|^ui-/.test(name)) return "implementation";
  if (/testing|test-driven|reproduce-bug/.test(name)) return "testing";
  if (/review|quality|simplify/.test(name)) return "review";
  if (/deployment|azdo-git|release|cicd/.test(name)) return "deployment";
  if (/^vbounce$/.test(name)) return "all";
  return "implementation";
}

// CLI entry point
if (require.main === module) {
  const target = process.argv[2] || process.cwd();
  buildRegistry(path.resolve(target));
}

module.exports = { buildRegistry };
